//variable que indica el indice de una fila
let indice = 5;

//condicional que determina si el numero es par o impar
//si es par se le aplicaría fondo gris
//si es impar se le aplicaría un fondo blanco
if (indice % 2 === 0) {
    console.log("La fila es PAR. Se le aplicará un fondo gris");
} else {
    console.log("La fila es IMPAR. Se le aplicará un fondo blanco.");
}